void	ft_swap(int *a, int *b)
{
	int	swap;

	swap = *a;
	*a = *b;
	*b = swap;
}
int main()
{
	int a = 10;
	int b = 5;
	int *p1 = &a;
	int *p2 = &b;
	ft_swap(p1,p2);
	
}